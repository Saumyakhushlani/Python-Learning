# ðŸ”¹ 4 Pillars of OOP
# ---------------------------------------------------------------------
# 1. Encapsulation
#    - Wrapping data and methods into a single unit (class)
#    - Keeps data safe from outside interference

# 2. Abstraction
#    - Hiding internal details and showing only essential features
#    - Reduces complexity and increases security

# 3. Inheritance
#    - One class can inherit properties and methods from another
#    - Promotes code reusability

# 4. Polymorphism
#    - Same function or method behaves differently in different contexts
#    - Example: same method name but different behavior in different classes

